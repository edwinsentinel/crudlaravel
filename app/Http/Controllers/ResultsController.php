     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
          'student_name'=>'required',
          'student_number'=> 'required|integer',
          'student_marks' => 'required|integer'
        ]);
        $results = new results([
            'student_name' => $request->get('student_name'),
            'student_number'=> $request->get('student_number'),
            'student_marks'=> $request->get('student_marks')
        ]);
        $results->save();
        return redirect('/results')->with('success', 'Student has been added');
      }
